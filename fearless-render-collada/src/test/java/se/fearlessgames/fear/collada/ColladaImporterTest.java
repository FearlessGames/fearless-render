	private void assertEqualsBuffer(IntBuffer buffer1, IntBuffer buffer2) {
		buffer1.rewind();
		buffer2.rewind();
		assertEquals(buffer1.limit(), buffer2.limit());

		for (int i = 0; i < buffer1.limit(); i++) {
			assertEquals(buffer1.get(), buffer2.get());
		}
	}


	private void assertEqualsBuffer(FloatBuffer buffer1, FloatBuffer buffer2) {
		buffer1.rewind();
		buffer2.rewind();
		assertEquals(buffer1.limit(), buffer2.limit());

		for (int i = 0; i < buffer1.limit(); i++) {
			assertEquals(buffer1.get(), buffer2.get(), 0.00001);
		}
	}

	private void assertEqualsMatrix(Matrix4 matrix1, Matrix4 matrix2) {
		if (matrix1 == null && matrix2 == null) {
			return;
		}

		if (matrix1 == null || matrix2 == null) {
			assertEquals(matrix1, matrix2);
			return;
		}

		for (int x = 0; x < 4; x++) {
			for (int y = 0; y < 4; y++) {
				String fail = "Matrix4[" + x + "][" + y + "]";
				assertEquals(fail, matrix1.getValue(x, y), matrix2.getValue(x, y), 0.0001);
			}
		}
	}

	private void assertEqualsData(int[] ints, IntBuffer buffer) {
		buffer.rewind();
		assertEquals(ints.length, buffer.limit());
		for (int v : ints) {
			assertEquals(v, buffer.get());
		}
	}

	private void assertEqualsData(float[] floats, FloatBuffer buffer) {
		buffer.rewind();
		assertEquals(floats.length, buffer.limit());
		for (float v : floats) {
			assertEquals(v, buffer.get(), 0.0001f);
		}
	}

